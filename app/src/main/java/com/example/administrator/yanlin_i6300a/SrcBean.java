public class SrcBean {
    String src;
    String time;

    public SrcBean(String src, String time) {
        this.src = src;
        this.time = time;
    }

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

}
